n1 = float(input("Digite um número: ")) #base do primeiro digito!!
n2 = float(input("Digite outro número: ")) #base do segundo digito!!

print("Soma:", n1 + n2)
print("Subtração:", n1 - n2)
print("Multiplicação:", n1 * n2)
print("Divisão:", n1 / n2 if n2 != 0 else "definitamente, n da pra dividir por zero")
print("Exponenciação:", n1 ** n2)
